package com.tenneti.thousie.model;

public class RandomHousieObject {
	private int Row;
	private int Column;
	private String HousieText;
	public int getRow() {
		return Row;
	}
	public void setRow(int row) {
		Row = row;
	}
	public int getColumn() {
		return Column;
	}
	public void setColumn(int column) {
		Column = column;
	}
	public String getHousieText() {
		return HousieText;
	}
	public void setHousieText(String housieText) {
		HousieText = housieText;
	}
}
